#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp;
	size_t			i;

	tmp = (unsigned char*)s;
	i = -1;
	while (++i < n)
	{
		if (tmp[i] == (unsigned char)c)
			return (tmp + i);
	}
	return (NULL);
}
