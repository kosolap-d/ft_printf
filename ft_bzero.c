#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = s;
	i = -1;
	if (n == 0)
		return ;
	while (++i < n)
		tmp[i] = 0;
}
