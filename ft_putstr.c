#include <unistd.h>

void	ft_putstr(char const *s)
{
	char	c;
	int		i;

	i = 0;
	if (s == 0)
		return ;
	while (s[i])
	{
		c = s[i];
		write(1, &c, 1);
		i++;
	}
}
