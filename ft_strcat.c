char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*p;

	i = 0;
	j = 0;
	p = (char*)s2;
	while (s1[i])
		i++;
	while (s2[j])
	{
		s1[i++] = s2[j++];
	}
	s1[i] = '\0';
	return (s1);
}
