#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int	tr;
	char		c;

	tr = nb;
	if (tr < 0)
	{
		write(1, "-", 1);
		tr *= -1;
	}
	if (tr > 9)
	{
		ft_putnbr(tr / 10);
	}
	c = tr % 10 + '0';
	write(1, &c, 1);
}
